    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nomeItem = jblnomeitem.getText();
        double valorItemCerto = Double.parseDouble(jblvaloritem.getText());

        if (jblcomida.isSelected()) {
            Comida comida = new Comida(nomeItem, valorItemCerto);
            comanda.incluirItem(comida, comanda);
        } else if (jblbebida.isSelected()) {
            Bebida bebida = new Bebida(nomeItem, valorItemCerto);
            comanda.incluirItem(bebida, comanda);
        } else if (jblcouvert.isSelected()) {
            Couvert couvert = new Couvert(nomeItem, valorItemCerto);
            comanda.incluirItem(couvert, comanda);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

