    public Turno identificarTurno () {
        if (jblmatutino.isSelected()) {
            return Turno.MATUTINO;
            
        } else if (jblvespertino.isSelected()) {
            return Turno.VESPERTINO;
            
        } else if (jblnoturno.isSelected()) {
            return Turno.NOTURNO;
            
        }
        return null;
    }
    
    
    public Titulacao identificarTitulacao () {
        if (jblgraduacao.isSelected()) {
            return Titulacao.GRADUACAO;
            
        } else if (jblmestrado.isSelected()) {
            return Titulacao.MESTRADO;
            
        } else if (jbldoutorado.isSelected()) {
            return Titulacao.DOUTORADO;
            
        }
        return null;
    }
    
    
    public void incluirAluno (Aluno aluno, String disciplina, ArrayList<Turma> turmas) {
        boolean turmaEncontrada = false;
        
        for (Turma a : turmas) {
            if (a.getDisciplina().equalsIgnoreCase(disciplina)) {
                a.getAlunos().add(aluno); //não sei se vai dar certo
                turmaEncontrada = true;
                break;
            }
        }
        
        if (turmaEncontrada != false) {
            JOptionPane.showMessageDialog(null, "Aluno foi incluído em " + disciplina + "!");
        } else if (turmaEncontrada != true) {
            JOptionPane.showMessageDialog(null, "Turma não foi encontrada");
        }
    }

    
