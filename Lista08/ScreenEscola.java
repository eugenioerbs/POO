    private void jblcriarturmaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jblcriarturmaActionPerformed
                    String disciplina = jbldisciplina.getText();                   
                    Turno turno = identificarTurno();
                    String nomeProf = jblnomeprof.getText();
                    String email = jblemail.getText();                            
                    Titulacao titulacao = identificarTitulacao();
                    
                    try {
                        professor = new Professor(nomeProf, email, titulacao);
                        turma = new Turma(disciplina, turno, professor);
                        turmas.add(turma);
                        JOptionPane.showMessageDialog(this, "Turma criada!");
                                              
                    } catch (IllegalArgumentException e) {
                        JOptionPane.showMessageDialog(null, e.getMessage());
                    }
               
    }//GEN-LAST:event_jblcriarturmaActionPerformed

