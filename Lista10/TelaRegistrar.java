    private void jblconfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jblconfirmarActionPerformed
        try {
            empresa = new Empresa(jblnomeempresa.getText());
        
        if (jblprogramadorbg.isSelected()) {
            programador = new Programador(jblnomefuncionario.getText(), Double.parseDouble(jblsalariobase.getText()));
            empresa.incluirFuncionario(programador);
            TelaProgramador telaProgramador = new TelaProgramador(programador);
            telaProgramador.setVisible(true);
            
        } else if (jblvendedorbg.isSelected()) {
            String nomeVendedor = jblnomefuncionario.getText();
            double salarioBase = Double.parseDouble(jblsalariobase.getText());
            TelaVendedor telaVendedor = new TelaVendedor(nomeVendedor, salarioBase, empresa);
            telaVendedor.setVisible(true);
        } else {
            outro = new Funcionario1(jblnomefuncionario.getText(), Double.parseDouble(jblsalariobase.getText()));
            empresa.incluirFuncionario(outro);
            JOptionPane.showMessageDialog(this, "Funcionário registrado com sucesso!");
            
        }
            
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
               
    }//GEN-LAST:event_jblconfirmarActionPerformed

