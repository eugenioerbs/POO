    private void jblexibirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jblexibirActionPerformed
        StringBuilder dados = new StringBuilder();
        
        for (Funcionario1 a : empresa.getFuncionarios()) {
            dados.append("Nome: ").append(a.getNome()).append(" - Salario base: R$ ").append(a.getSalarioBase()).append("\n");
            
            if (a instanceof Programador) {
                Programador p = (Programador) a;
                for (String s : p.getLinguagens()) {
                    dados.append(s).append(" - ");
                }
                dados.append("\n");
            } else if (a instanceof Vendedor) {
                Vendedor v = (Vendedor) a;
                dados.append("Comissão: ").append(v.getPercentualComissao()).append("\n");
                for(Venda venda : v.getVendas()) {
                    dados.append(venda.getValor()).append(" - ");
                }
                dados.append("\n");
            }
            dados.append("Salario: ").append(a.calcularSalario()).append("\n");
            
        }
        JOptionPane.showMessageDialog(this, dados.toString());
        
    }//GEN-LAST:event_jblexibirActionPerformed

